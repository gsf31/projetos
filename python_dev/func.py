def contar_caracteres(x, y, z):
    total_caracteres = len(x) + len(y) + len(z)
    return total_caracteres

texto1 = "Olá, mundo!"
texto2 = "Este é um exemplo de código."
texto3 = "Espero que isso ajude."
resultado = contar_caracteres(texto1, texto2, texto3)
print("O número total de caracteres é:", resultado)



