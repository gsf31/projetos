// Média >= 7 (Aprovado)
// Média < 7 (Reprovado)

const ava1 = 5;
const ava2 = 3;
const ava3 = 7;

let media = (ava1 + ava2 + ava3) /  3;

if (media >= 7) {
    console.log("Sua média foi", media, "você foi Aprovado")
} else {
    console.log("Sua média foi", media, "você vai foi Reprovado")
}

